import java.util.*;
class patterns{
    public static void main(String args[]){
        Scanner n=new Scanner(System.in);
        int num=n.nextInt();
        for(int i=0;i<num;++i){
            for(int j=0;j<num;++j){
            System.out.print("*");}
            System.out.println("*");
        }
    }
}